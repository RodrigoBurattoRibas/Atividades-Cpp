#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "Portuguese");

    int num = 0, cont = 0;

    cout << "Digite um número inteiro e eu direi a quantidade de dígitos que ele contém:" << endl;
    cin >> num;

    if (num <= 0) {
        cout << "Digite apenas números positivos.";
        return 0;
    }
    while (num > 0) {
        num = num / 10;
        cont++;
    }
    cout << "Esse número tem " << cont << " números.";
    return 0;
}